/** Порядок секций: A — как в ТЗ; B — отзывы вверху; C — процесс вверху. */
const LAYOUT_ORDER: Record<SoloLayoutId, SoloSectionKind[]> = {
  A: ["benefits", "how", "services", "reviews", "trust", "final_cta"],
  B: ["reviews", "benefits", "services", "trust", "final_cta"],
  C: ["how", "benefits", "services", "reviews", "trust", "final_cta"],
};

function layoutFromSeed(mixKey: string): SoloLayoutId {
  const i = pickIndex(`${mixKey}|layout`, 3);
  return i === 0 ? "A" : i === 1 ? "B" : "C";
}

function variantForSection(kind: SoloSectionKind, mixKey: string, pos: number): SoloSectionVariant {
  if (kind === "how") return "steps";
  if (kind === "final_cta") return "accent";
  if (kind === "trust") {
    return pickIndex(`${mixKey}|sv|${pos}|trust`, 2) === 0 ? "accent" : "cards";
  }
  return pickIndex(`${mixKey}|sv|${pos}|def`, 2) === 0 ? "cards" : "list";
}

function buildSections(layoutId: SoloLayoutId, mixKey: string): SoloSection[] {
  const kinds = LAYOUT_ORDER[layoutId];
  return kinds.map((kind, pos) => ({
    kind,
    variant: variantForSection(kind, mixKey, pos),
  }));
}

