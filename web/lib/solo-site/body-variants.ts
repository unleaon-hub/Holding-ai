const BY_PRESET: Record<PresetId, BodyPools> = {
  beauty: BEAUTY,
  repair: REPAIR,
  legal: LEGAL,
  medical: MEDICAL,
  default: DEFAULT,
};

function interpCity(s: string, city: string): string {
  return s.replace(/\{city\}/g, city.trim() || "город");
}

function buildBlock(p: BodyPools, mixKey: string, city: string): SoloBodyBlocks {
  const tWhy = p.whyTitles[pickIndex(`${mixKey}|b|whyT`, p.whyTitles.length)] ?? p.whyTitles[0]!;
  const tServ = p.servicesTitles[pickIndex(`${mixKey}|b|st`, p.servicesTitles.length)] ?? p.servicesTitles[0]!;
  const tTrust = p.trustTitles[pickIndex(`${mixKey}|b|trT`, p.trustTitles.length)] ?? p.trustTitles[0]!;

  const nWhy = 3 + pickIndex(`${mixKey}|b|nwhy`, 2);
  const nSvc = 3 + pickIndex(`${mixKey}|b|nsvc`, 3);
  const nTrust = 2 + pickIndex(`${mixKey}|b|ntrust`, 2);

  const whyItems = takeFromShuffledPool(mixKey, "wpool", p.whyPool, nWhy).map((x) => interpCity(x, city));
  const serviceItems = takeFromShuffledPool(mixKey, "spool", p.servicePool, nSvc).map((x) => interpCity(x, city));
  const trustItems = takeFromShuffledPool(mixKey, "tpool", p.trustPool, nTrust).map((x) => interpCity(x, city));

  const fIdx = pickIndex(`${mixKey}|b|fin`, p.finalCta.length);
  const rawFinal = p.finalCta[fIdx] ?? p.finalCta[0]!;
  const finalCtaTitle = interpCity(rawFinal.title, city);
  const finalCtaMicro = interpCity(rawFinal.micro, city);
  const finalCtaButton = rawFinal.button;
  const fk = p.footerKickers[pickIndex(`${mixKey}|b|ft`, p.footerKickers.length)] ?? p.footerKickers[0]!;

  const footerKicker = interpCity(fk, city);

  return {
    whyTitle: tWhy,
    whyItems,
    servicesTitle: tServ,
    serviceItems,
    trustTitle: tTrust,
    trustItems,
    finalCtaTitle,
    finalCtaMicro,
    finalCtaButton,
    footerKicker,
  };
}

export function buildSoloBodyBlocks(args: {
  city: string;
  mixKey: string;
  /** Пул текстов: пресет hero или `default` для нейтрального. */
  bodyPreset: PresetId;
}): SoloBodyBlocks {
  const p = BY_PRESET[args.bodyPreset] ?? DEFAULT;
  return buildBlock(p, args.mixKey, args.city);
}

/** Превью для сессий без `soloBlocks`: дет. от niche+city. */
export function getLegacyFallbackSoloBodyBlocks(niche: string, city: string): SoloBodyBlocks {
  const pid = selectPresetId(niche, false);
  const mixKey = `${niche}\0${city}\0legacy\0${pid}\0v0`;
  return buildSoloBodyBlocks({ city, mixKey, bodyPreset: pid });
}
